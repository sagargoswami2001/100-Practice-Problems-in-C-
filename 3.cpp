//C++ Program of find Even & Odd Number.
#include<iostream>
using namespace std;
int main()
{
    int a;
    cout<< "Enter Number: ";
    cin>> a;
    if(a%2==0)
    {
        cout<< "The Number is Even";
    }
    else
    {
        cout<< "The Number is Odd";
    }
    return 0;
}
