//Program to explain Cascading of Insertion (<<) Operator.
//Operator and Cascading of Extraction (<<) Operator.
#include<iostream>
using namespace std;
int main()
{
    int num1,num2,sum;
    cout << "Enter Two Number:\n";
    cin >> num1 >> num2;
    sum = num1 + num2;
    cout << "\n" << num1 << " + " << num2 << " = " << sum;
}
